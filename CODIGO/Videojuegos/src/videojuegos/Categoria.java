    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (txtIdA.getText().equals("") && txtTipoA.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campos Vacios");
        } else {
            int opcion = JOptionPane.showConfirmDialog(null, "Seguro de los campos", "Donacion", JOptionPane.YES_NO_OPTION, JOptionPane.PLAIN_MESSAGE);
            if (opcion == JOptionPane.YES_OPTION) {
                d.insertarCategoriaProcedimiento(txtIdA.getText(), txtTipoA.getText());
                txtIdA.setText("");
                txtTipoA.setText("");
                DefaultTableModel modelo = d.MostrarCategorias();
                jTableCategorias.setModel(modelo);
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

