    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (txtIdE.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Vacio");
        } else {
            int opcion = JOptionPane.showConfirmDialog(null, "Seguro De Eliminar", "Donacion", JOptionPane.YES_NO_OPTION, JOptionPane.PLAIN_MESSAGE);
            if (opcion == JOptionPane.YES_OPTION) {
                d.eliminarCategoriaProcedimiento(txtIdE.getText().trim());
                txtIdE.setText("");
                DefaultTableModel modelo = d.MostrarCategorias();
                jTableCategorias.setModel(modelo);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void jTableCategoriasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableCategoriasMouseClicked
        int seleccion = this.jTableCategorias.getSelectedRow();
        //PESTAÑA DE MODIFICAR
        this.txtIdM.setText(jTableCategorias.getValueAt(seleccion, 0).toString());
        this.txtTipoM.setText(jTableCategorias.getValueAt(seleccion, 1).toString());
        //PESTAÑA DE ELIMINAR
        this.txtIdE.setText(jTableCategorias.getValueAt(seleccion, 0).toString());
    }//GEN-LAST:event_jTableCategoriasMouseClicked

