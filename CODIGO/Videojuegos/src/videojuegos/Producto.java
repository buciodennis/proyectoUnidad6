    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (txtIdProP.getText().equals("") && txtNombreP.getText().equals("")
                && txtDescripP.getText().equals("") && txtPrecioP.getText().equals("")
                && txtExistenciaP.getText().equals("") && txtCategoriaP.getText().equals("")
                && txtProveedorP.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campos Vacios");
        } else {
            int opcion = JOptionPane.showConfirmDialog(null, "Seguro De Esta Accion", "Donacion", JOptionPane.YES_NO_OPTION, JOptionPane.PLAIN_MESSAGE);
            if (opcion == JOptionPane.YES_OPTION) {
                d.insertarProducto(txtIdProP.getText(), txtNombreP.getText(),
                        txtDescripP.getText(), txtPrecioP.getText(),
                        txtExistenciaP.getText(), txtCategoriaP.getText(), txtProveedorP.getText());
                txtIdProP.setText("");
                txtNombreP.setText("");
                txtDescripP.setText("");
                txtPrecioP.setText("");
                txtExistenciaP.setText("");
                txtCategoriaP.setText("");
                txtProveedorP.setText("");
                DefaultTableModel modelo = d.MostrarProductos();
                jTableProductos.setModel(modelo);
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

