    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (txtIdProE.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Vacio");
        } else {
            int opcion = JOptionPane.showConfirmDialog(null, "Seguro De Eliminar", "Donacion", JOptionPane.YES_NO_OPTION, JOptionPane.PLAIN_MESSAGE);
            if (opcion == JOptionPane.YES_OPTION) {
                d.eliminarProducto(txtIdProE.getText());
                txtIdProE.setText("");
                DefaultTableModel modelo = d.MostrarProductos();
                jTableProductos.setModel(modelo);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

